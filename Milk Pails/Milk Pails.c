int x,y,m;
#define Hd main(void)

int mi(int a, int b){
    return (a>b? b:a);
}

int memo[101][101][101];
int solve(int i,int j, int k){
    if(!memo[i][j][k]){
        memo[i][j][k]= (m-i-j <0? -m+i+j:m-i-j);
    }
    else{
        return memo[i][j][k];
    }
    if(!k){
        int a1=m-(i+j);
        a1 = (a1<0? -a1:a1);
        return a1;
    }
    int a=1000;
    if(i<x)a=mi(solve(x,j,k-1),a);
    if(j<y)a=mi(a,solve(i,y,k-1));
    if(i>0 && j<y){
        if(j+i<=y){
            a=mi(a,solve(0,j+i,k-1));
        }
        else{
            a=mi(a,solve(j+i-y,y,k-1));
        }
    }
    if(j>0 && i<x){
        if(j+i<=x){
            a=mi(a,solve(i+j,0,k-1));
        }
        else{
            a=mi(a,solve(x,i+j-x,k-1));
        }
    }
    if(j)a=mi(a,solve(i,0,k-1));
    if(i)a=mi(a,solve(0,j,k-1));
        
    return a;
}

